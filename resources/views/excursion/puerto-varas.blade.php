<p class="destination-title">City Tour Puerto Montt Puerto Varas</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día</span>

<p>Centro turístico del Sur de Chile, que se caracteriza por sus atractivos prados y rosales. Recorrido por la Av. Costanera, visita a la feria artesanal e iglesia, más tarde proseguiremos viaje a Puerto Montt, capital de la región de los Lagos visitando en el trayecto Plaza de Armas, la Catedral construida en madera de Alerce inspirando su diseño en el Partenón de Atenas, la Capilla San Francisco de Sales, monumento a la Colonización Alemana y la Caleta de Angelmo con tiempo para visitar su mercado.
</p>
<br>

<br><br>


<p class="destination-title">Frutillar</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día</span>

<p>Localidad ubicada a orillas del Lago Llanquihue con magnifica vista al Volcán Osorno. Fundada en 1856 y poblada por familias alemanas. Cuenta con dos Iglesias (Católica y Luterana) y un Museo Colonial Alemán. Localidad famosa por las Semanas Musicales, de prestigio internacional, entre fines de enero e inicios de febrero se transforma en un importante centro cultural, hasta allá llegan orquestas sinfónicas, grupos de cámara y directores de varios países.
</p>
<br>

<br><br>


<p class="destination-title">Volcán Osorno y Petrohue</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día</span>

<p>Una de las excursiones más maravillosas del Sur de Chile, ubicado dentro del Parque Nacional Vicente Perez Rosales se encuentran los Saltos del Rio Petrohue, que pasan por estrechos canales naturales de lava dando un marco de impresionante belleza. Visita al Lago Todos los santos, (lugar donde nace el Rio Petrohue) cuya característica el color Esmeralda de sus aguas, montanas verdes y bosques densos. Debe su nombre al descubrimiento hecho por los Jesuitas el 1 de Noviembre de 1670.
</p>
<br>

<br><br>



<p class="destination-title">Ancud</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo</span>

<p>Salida a la Isla de Chiloe. Es la isla de las iglesias de madera, de los artesanos, playas, tierra de pescadores y navegantes. Para acceder a ella se cruza desde el continente en transbordador a través del Canal de Chacao. Ancud es la puerta de entrada a la Mágica Isla Grande. Fundada por Carlos Berenger como fortaleza y puerto el año 1767. Visita al complejo turístico museológico "Chilotur" muestra de artesanías, objetos históricos, una réplica de la Goleta Ancud que en 1843 llevo colonos hasta Magallanes y en un patio, figuras de la mitología isleña, lo último en visitar es el Fuerte de San Antonio.
</p>
