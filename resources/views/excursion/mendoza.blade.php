<span><strong>Duraci&oacute;n:</strong> de 07:00hs a 20.00hs aprox. (día completo)</span>
<br>
<p>Visitar, caminar y disfrutar de la inmensidad de la montaña es lo que nos propone el camino, destacado por su riqueza paisajística. Salida hacia el sur de la ciudad de Mendoza para tomar la Ruta Nacional 7 que nos llevará al camino de Alta Montaña. Pasaremos por parajes de extraordinaria belleza en la zona de Cacheuta, la Villa de Potrerillos y el Valle de Uspallata. Visita al histórico puente de Picheuta. Pasaremos por  Polvaredas, Punta de Vacas. Luego llegaremos al centro de ski Los Penitentes y poco después a Puente del Inca con su fascinante formación geológica. Visitaremos el Parque Provincial Aconcagua, para obtener desde allí una inmejorable vista del Cerro Aconcagua. Continuación hasta Las Cuevas, último poblado argentino. Regreso a Mendoza por el mismo camino.- Al Cristo Redentor solamente es posible llegar en los meses de diciembre a febrero.
