<span><strong>Salidas:</strong> Diarias</span><br>
<span><strong>Duraci&oacute;n:</strong> 9 a 19hs aproximadamente</span>
<br>
<p>En el tour se visitarán 3 bodegas emblemáticas del Valle de Uco (Atamisque, Diamandes, Domaine Bousquet, La Azul, Pulenta Estate) con almuerzo (en Atamisque, Diamandes, Domaine Bousquet o La Azul) maridado con vino de la bodega y tour en la bodega con guía especializado. La degustación será de la línea estándar o básica de la bodega.
