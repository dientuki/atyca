<p class="destination-title">Caminos del Vino Maipú - Full day Clásico con almuerzo</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> 9 a 17hs aproximadamente.</span>
<br>
<p>En el tour se visitar&aacute;n 3 bodegas del departamento de Maip&uacute; incluyendo almuerzo maridado con vinos de la bodega y tour en cada bodega con gu&iacute;a especializado. La degustaci&oacute;n ser&aacute; de la l&iacute;nea cl&aacute;sica o est&aacute;ndar de la bodega.</p>
<p>Se puede optar entre: Carinae, Don Manuel Villafa&ntilde;e, L&oacute;pez, Sin Fin, Trapiche, Trivento o Zuccardi. El Almuerzo puede ser en L&oacute;pez o Zuccardi. Las tres bodegas son a elecci&oacute;n del pasajero; si no tuviera predilecci&oacute;n, se le sugerir&aacute;.</p>
<p>&nbsp;</p>
<p>A tan solo 15 kil&oacute;metros de la ciudad de Mendoza se encuentra la coqueta ciudad de Maip&uacute;, cuya tradici&oacute;n bodeguera se ve hoy salpicada por nuevos emprendimientos tur&iacute;sticos familiares, bodegas, muchas de ellas enfocadas ahora en la gastronom&iacute;a y la producci&oacute;n de vinos org&aacute;nicos.</p>
<p>El clima de Maip&uacute; es templado &aacute;rido, con tendencias a c&aacute;lido. Este clima tiene consecuencias directas en los cultivos, en su mayor&iacute;a de la vid. Sin embargo, unos sinn&uacute;meros de acequias transportan agua a todas estas tierras. Maip&uacute; es parte del Gran Mendoza y junto con Luj&aacute;n forman la &ldquo;Primer Zona Vitivin&iacute;cola&rdquo;, se cultivan uvas de alta calidad</p>
<p>La Ruta del Vino es uno de los atractivos que tiene la ciudad. A&ntilde;o tras a&ntilde;o se suman nuevas alternativas a los circuitos tradicionales, para conocer las bodegas.</p>
<p>La Ruta del Olivo es otro de los grandes aciertos de esta ciudad. Recorriendo diversos emprendimientos familiares se aprende todo sobre la aceituna y los productos y derivados que provienen de ella, entre estos el codiciado aceite de oliva.</p>
