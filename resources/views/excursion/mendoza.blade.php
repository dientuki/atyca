<span><strong>Duraci&oacute;n:</strong> de 14:00hs a 19.00hs aprox. (medio día)</span>
<br>
<p>Recorrido por el oasis agrícola mendocino y zonas de cultivo de la vid. Visitaremos 2 importantes y tradicionales establecimientos vitivinícolas, Vistandes y Don Arturo, la primera más industrializada y de mayor envergadura, en la segunda podremos observar procedimientos tradicionales, ya que se trata de una bodega mucho más pequeña y artesanal. En ambas tendremos oportunidad de degustar sus exquisitos productos.
