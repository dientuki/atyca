<p class="destination-title"><strong>EXCURSIONES EN SALTA</strong></p>
<br><br>

<p class="destination-title">City Tour </p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong>3 horas (pick up 16.00 / 16.30hs aprox.)</span>

<p>Recorrido por los sitios de mayor atractivo de la ciudad: Catedral Basílica de 1855 y donde se encuentran los restos del Gral. Güemes, Centro Cultural de las América, Museo Histórico del Norte (Cabildo) con exhibiciones de arte sacro y salas dedicadas a antiguas familias salteñas, Basílica Menor de San Francisco, Convento San Bernardo de la orden de las Carmelitas, Monumento a Güemes, ascenso Cerro San Bernardo y su vista panorámica de la ciudad, paso por el monumento 20 de Febrero donde se llevó a cabo la Batalla de Salta) y Parque, Visita a la villa veraniega San Lorenzo en las afueras de la ciudad con su microclima subtropical y el Mercado Artesanal donde son expuestas artesanías con certificación de origen de toda la provincia.
</p>
<br>

<br><br>



<p class="destination-title">Cafayate </p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo (pick up 7.00 / 7.30hs aprox.)</span>

<p>El recorrido comienza por el Valle de Lerma atravesando campos de cultivo de tabaco y pueblos de arquitectura colonial como Alemania, que solía ser el punto final del ferrocarril Belgrano. Ingreso a la Quebrada del Río Las Conchas donde las curiosas formaciones erosionadas por el viento y el agua serán el principal atractivo: los médanos, el sapo, el obispo, la garganta del diablo, los castillos, el anfiteatro, entre otros. Luego se continúa por la ruta Nacional 68 llegando a Cafayate, la ciudad más joven de los Valles Calchaquíes reconocida a nivel internacional por las cepas de sus vinos, en particular el Torrontés, que podrá ser degustado en las visitas a sus bodegas, ya sean artesanales o de tipo industrial. Se regresa a Salta por la misma ruta, pero con una luz solar completamente distinta cuya iluminación modificará el paisaje en la quebrada por completo.
</p>
<br>

<br><br>




<p class="destination-title">Quebrada de Humahuaca </p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo (pick up 7.00 / 7.30hs aprox.)</span>

<p>Un viaje por la Quebrada declarada Patrimonio de la Humanidad, donde el paisaje y los colores de los cerros se conjugan con las culturas igualmente coloridas que habitan esta zona. Es un viaje de contacto social y una vista a lo ancestral. Comienza con la llegada al pueblito de Purmamarca para observar el Cerro de los Siete Colores. Paso por la Posta de Hornillos, continuando hacia el pueblo de Tilcara. Continua hacia Huacalera, paso por el Trópico de Capricornio, desde donde se pueden ver los colores del cerro llamado la Pollera de la Colla. Visita a la Iglesia de Uquia, donde se encuentran las pinturas de los Angeles Arcabuceros (vestigio importante de la zona de la Escuela Cuzqueña). Arribo a Humahuaca con su importante Catedral y el Monumento a la Independencia (El Indio) del escultor Soto Avendaño. Regreso después del almuerzo para observar desde la ruta la Paleta del Pintor que enmarca el pueblo de Maimara. Arribo a San Salvador de Jujuy en donde se visitará el Salón de la Bandera y la Iglesia Catedral, con su característico pulpito tallado en madera y laminado en oro y las estatuas de Lola Mora.
</p>
<br>

<br><br>





<p class="destination-title">Tafi del Valle</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día</span>

<p>Visitamos Capilla y Convento Jesuíticos de San Jose de Lules donde se conservan la capilla y el claustro. La Ruta 307 marca el ascenso a los Valles. El camino se interna en la Quebrada del rio Los Sosa, parte de la Reserva Natural del mismo nombre. La selva deslumbra e invita a detenerse. Continuando, avistamos el Monumento El Indio. Más arriba, la entrada al Valle de Tafi es simplemente impactante. Rodeando el Embalse La Angostura llegamos a El Mollar para visitar Los Menhires. Tafi del Valle se encuentra a 2000 msnm. Aquí se conserva el Conjunto Jesuítico de la Banda, cuyo sector más antiguo fue levantado por los jesuitas en la primera mitad del Siglo XVIII. Tiempo libre para almorzar. Continuamos camino por la RP 307 hasta alcanzar el mirador desde donde se obtienen las mejores vistas del valle.
</p>
<br>

<br><br>





<p class="destination-title">Purmamarca y Salinas Grandes</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo</span>

<p>Salida desde Salta por la mañana, para llegar a primeras horas al pueblo de Purmamarca, con su Cerro de Siete Colores, la Iglesia y el Mercado Artesanal de la plaza. Luego de recorrer el pueblo se asciende por la Cuesta de Lipan hasta llegar a las impresionantes Salinas Grandes, uno de los destinos más visitados de la región de la Puna de Jujuy, un lugar increíble donde el paisaje blanco se confunde con el cielo azul intenso, allí se puede observar el proceso de extracción de la sal de mesa. Se regresa a Salta por el mismo camino.
</p>
<br>

<br><br>





<p class="destination-title">Tren de las Nubes</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo</span>

<p>El tour comienza en las Estación de Trenes de San Antonio de los Cobres donde los viajeros se embarcarán en el Tren a las Nubes que los llevará al Viaducto la Polvorilla. El recorrido será de una hora hasta el Viaducto, a 4.200 msnm, donde se atraviesa la obra más imponente de la ingeniería del siglo pasado.
</p>
