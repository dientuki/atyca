<strong><u>Delta Premium</u></strong>

<u>Salidas:</u> Diarias AM / Lunes a Sábados PM

<u>Duración</u>: medio día

&nbsp;

Descubra nuevos paisajes del Delta navegando desde o hacia la Costanera Metropolitana. Salimos desde Buenos Aires, visitamos San Isidro, y al llegar al Delta nos embarcamos para descubrir nuevos horizontes. En el recorrido veremos los accesos a los clubes náuticos a la vera del río Luján; las sorprendentes casas y mansiones ubicadas en las barrancas y sus magníficas vistas del estuario del plata. Entre las principales referencias ubicadas en la costa destacamos la Catedral de San Isidro; el Puerto de Olivos; el estadio Monumental de River; la Ciudad Universitaria de Buenos Aires, y el permanente desfile aéreo de partidas y arribos al Aeropuerto Metropolitano. Anímese!, es un paseo inigualable.

