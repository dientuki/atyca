<strong><u>Cena Show de Tango en TANGO PORTEÑO</u></strong>

&nbsp;

Tango Porteño, un lugar único e irrepetible...

Recrea mágicamente la época más importante y trascendente de la historia del tango, enmarcándola lujosamente en un edificio de aquellos años que supo ser un cine-teatro propiedad de la Metro-Goldwyn-Meyer. El viejo y querido Metro, a sólo pocos pasos del Obelisco, hoy se transforma en Tango Porteño, un lugar que combina una refinadísima ambientación y rescata el puro estilo deco de la época de oro complementándose con un elenco artístico, producciones insuperables y un despliegue gastronómico de gran calidad que hace honor a la época que representa. De otra forma no podía ser, porque Tango Porteño es la imagen de Buenos Aires, la "Ciudad del Tango".

&nbsp;

&nbsp;

<strong><u>Cena Show de Tango en LA VENTANA</u></strong>

&nbsp;

En pleno corazón de San Telmo, La Ventana Barrio de Tango, abrió sus puertas al mundo en 1982. Este histórico conventillo totalmente restaurado es un clásico recorrido por la cultura porteña a través de los tiempos. Los interiores, que preservan lo más puro del edificio, son el ámbito ideal para una propuesta única con 32 artistas en escena: dos orquestas de Tango, un conjunto de música folclórica, bailarines y cantantes. Nuestro menú incluye platos internacionales y típicos de nuestro país, con la tradicional parilla al carbón y una amplia bodega de vinos argentinos.

&nbsp;

&nbsp;

<strong><u>Cena Show de Tango en ESQUINA CARLOS GARDEL</u></strong>

&nbsp;

Es En la esquina de la cortada bautizada Carlos Gardel en su homenaje y la calle Anchorena, donde está hoy emplazada la estatua que lo inmortaliza, existía un restaurante que cobijaba entre sus paredes a los pintorescos personajes del mercado. Más de 100 años después, como por arte de magia, esta historia revive. En Buenos Aires cada esquina tiene un tango, pero el tango tiene una sola esquina.

&nbsp;

&nbsp;

<strong><u>Cena Show de Tango en ROJO TANGO</u></strong>

&nbsp;

Ubicado dentro del exclusivo e imponente Hotel Faena, Rojo Tango es una propuesta fascinante que conquista todos los sentidos, combinando sonidos, aromas, colores, movimiento y sabores.

Envueltos en terciopelo rojo se encuentran los más selectos bailarines y músicos de Tango de Argentina, junto a gastronomía de primer nivel internacional, para brindar al público un disfrute único.



