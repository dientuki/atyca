<p class="destination-title"><strong>EXCURSIONES EN PUERTO MADRYN</strong></p>
<br><br>

<p class="destination-title">Península Valdés</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día / 10 hs.</span>
<br>
<p>Esta excursión se inicia en Puerto Madryn a las 7:15/7:45hs en verano y 8/8:30hs en invierno pasando por el alojamiento de cada pasajero. Se trata de un recorrido terrestre de 380 Km., acompañados por guías naturalistas.  Península Valdés, Patrimonio Mundial de la Humanidad, encierra a lo largo de su extensión diferentes especies únicas que encuentran en la zona un lugar propicio para desarrollar su vida. Visita al Centro de Interpretación Istmo C. Ameghino, exhibición sobre la flora y fauna del lugar; Puerto Pirámides, villa turística desde donde zarpan las embarcaciones para realizar el paseo náutico con snorkelling durante la temporada de verano; Caleta Valdés y Punta Cantor, observación de lobos y elefantes marinos, pingüinos de Magallanes; y Punta Norte, colonia de lobos marinos. No incluye ingreso a la reserva, comidas, paseo náutico y todo servicio no especificado.
 </p>
<br><br>



<p class="destination-title">Avistaje de Ballenas</p>
<span><strong>Salidas:</strong> Diarias de Junio a Diciembre, cada 2 hs. </span><br>
<span><strong>Duraci&oacute;n:</strong> 2 hs. aprox.</span>
<br>
<p>Desde Puerto Pirámides, a 100 Km. de Puerto Madryn, parten las embarcaciones para el avistaje de ballenas. Los capitanes y la tripulación especializada, introducirán a los pasajeros en la interpretación del ecosistema costero y marino. Asistidos por personal auxiliar de a bordo, bilingüe. Esta navegación está catalogada como la de mayor disfrute naturalista de la Península Valdés. Durante la navegación, aprenderemos sobre el ciclo biológico de las ballenas: apareamiento, nacimientos y crianza del ballenato.
*Condiciones climáticas pueden modificar los horarios de salida.
  </p>
<br><br>




<p class="destination-title">Punta Tombo</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo</span>
<br>
<p>En esta excursión, visitaremos la pingüinera más importante de Latinoamérica. Se combina con la magnífica experiencia de la observación de toninas en su hábitat natural. Comenzando desde las 7:15/7:45hs, buscamos a cada pasajero por su alojamiento. Primera parada, Puerto de Rawson, Playa Unión, a las 09:00hs -  Optativo, avistaje de Toninas Overas, el delfín más pequeño del mundo. Hay que recordar que estos animales amistosos y juguetones viven en libertad por lo que los capitanes harán lo posible por encontrarlos, dependiendo esto de los delfines. Esta navegación dura aproximadamente 1:00 hora.<br>
Al regresar, tendremos tiempo para visitar el Puerto de Rawson, famoso por sus pescados y mariscos. A continuación, nos desplazaremos hacia Punta Tombo, accidente geográfico que sirve de hábitat del pingüino de Magallanes. Tiempo estimado de permanencia en esta reserva hasta: 2:00 hs.- Tendremos tiempo de observar como cohabitan en la costa, Gaviotas, Skúas, Petreles Gigantes, Cormoranes, Ostreros y pato Vapor y dada la extensión del área, especies características de la estepa como Guanacos, Choiques, Maras y Zorros. Visitaremos la localidad de Gaiman, para admirar su Cultura, Capillas de una arquitectura muy particular, degustar el Té Gales (opcional). No incluye ingreso a la reserva, comida, opcionales y todo servicio no especificado.
 
  </p>
<br><br>





<p class="destination-title">Avistaje de Toninas Overas</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> 1 hr.</span>
<br>
<p>Navegación en lancha semirrígida para observar este delfín, uno de los más pequeños del mundo. Incluye avistaje de aves como ostreros, cormoranes y gaviota capucha marrón. En los alrededores del puerto también podremos encontrar grupos pequeños de lobos marinos. Lugar de zarpada: Puerto de Pescadores de Playa Unión. Salidas sujetas a condiciones climáticas y nivel de marea. No incluye traslado terrestre, comidas y todo servicio no especificado.
 
 
  </p>
<br><br>





<p class="destination-title">City Tour + Lobería Punta Loma</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día / 2 hs. 30 min.</span>
<br>
<p>El City Tour por la ciudad nos permite recorrer el centro comercial, el casco histórico, los muelles mineraleros y de cruceros, concluyendo el mismo en el mirador panorámico de Punta Cuevas, junto al monumento al indio tehuelche, otrora habitante de esta zona. Desde allí se parte hacia playa Paraná, para arribar luego de unos kilómetros a lobería de Punta Loma, apostadero invernal continental de lobos marinos de un pelo. Pudiendo observar las colonias de adultos y juveniles desde acantilados preparados como balcones/miradores y deleitándonos con los vuelos de los cormoranes negros. No incluye entrada a la reserva, comida, opcionales y todo servicio no especificado.
 
 
  </p>
<br><br>




<p class="destination-title">Dique Ameghino + Bosque Petrificado</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Día completo</span>
<br>
<p>El Dique Florentino Ameghino es una obra de ingeniería hidráulica en un paisaje de colorida belleza natural. Realizaremos caminatas de interpretación sobre senderos naturales que usaron los pueblos originarios. Observaremos cuevas que dejaron su testimonio histórico. No incluye comidas, entrada a Bosque Petrificado y todo servicio no especificado.
 
  </p>
<br><br>






<p class="destination-title">Valle Inferior del Rio Chubut (Trelew + Gaiman)</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día – Pick up: 2PM aprox. Drop off: 8PM aprox.</span>
<br>
<p>Circuito histórico – cultural en la antigua colonia galesa Gaiman, teniendo la posibilidad de degustar del tradicional Te Gales. También visitaremos el Museo de los Dinosaurios – MEF – en la ciudad de Trelew. No incluye entrada a museo, comidas, opcionales y todo servicio no especificado.
 
  </p>
