<p class="destination-title">Cerro Catedral</p>
<span><strong>Salidas:</strong> Diarias - En Invierno consultar operatividad.</span><br>
<span><strong>Duraci&oacute;n:</strong> medio d&iacute;a</span>
<br>
<p>Esta excursión de medio día tiene como objetivo visitar el centro de deportes invernales de San Carlos de Bariloche. En dirección al Llao Llao nos desviamos en el Km 8 hasta llegar a Virgen de las Nieves donde tomamos el camino a la derecha; pasando por el Centro de Salmon y cultura llegamos a Villa Catedral, ubicada al pie del cerro. Posee, además de excelentes condiciones naturales, toda la infraestructura necesaria para la práctica de deportes invernales. De la base parten diversos medios de elevación que conducen a distintas plataformas, que cuentan con confiterías y restaurantes. </p>
<br><br>


<p class="destination-title">Cerro Tronador</p>
<span><strong>Salidas:</strong> Diarias – En Invierno sujeto a condiciones climáticas</span><br>
<span><strong>Duraci&oacute;n:</strong> día completo</span>
<br>
<p>Excursión de día completo. Se parte a las 9:00 hs hacia el sur y bordeamos los lagos Gutiérrez y Mascardi. Luego costeamos el brazo oeste del lago Mascardi por un camino sinuoso hasta Pampa Linda. Continuamos unos kilómetros en bus hasta llegar a un arroyo. Aquí comienza la caminata de 30 minutos aproximadamente: tras cruzar un puentecito colgante se recorre un bosque de lenga y caña colihue hasta llegar a un acantilado desde donde se puede observar el saltillo Las Nalcas, cuya caída de agua baja por una pared de piedra de aproximadamente 50 metros. Alrededor del saltillo se observan las grandes hojas de Nalca que llegan a medir hasta un metro de diámetro. Regresamos a tiempo para el almuerzo para continuar luego hacia el Mirador del Ventisquero Negro. A partir de las 15:45 horas se inicia el regreso a la ciudad. La llegada a los hoteles es a las 18:00 horas. De septiembre a noviembre hay gran cantidad de agua. De diciembre a abril, la caída de agua se hace más fina, semejante a pinceladas. De mayo a octubre, la excursión está condicionada por el estado de los senderos. No incluye almuerzo ni ingreso a Parques Nacionales. </p>
<br><br>



<p class="destination-title">Villa La Angostura y Cerro Bayo</p>
<span><strong>Salidas:</strong> Diaria. Consultar operatividad en época invernal.</span><br>
<span><strong>Duraci&oacute;n:</strong> día completo</span>
<br>
<p>Saliendo desde el hotel hacia el este llegamos al río Limay. Siguiendo tres kilómetros encontramos el desvío a Villa La Angostura, rodeados por la árida estepa patagónica y transitando por extensas estancias tomamos hacia el oeste hasta llegar al brazo Huemul. Los bosques de cipreses comienzan a aparecer lentamente para dar luego lugar al bosque húmedo. Dejando atrás al brazo Huemul y Bahía Manzano arribamos a Villa la Angostura. Esta villa de características únicas está situada sobre la costa norte del lago Nahuel Huapi, durante su recorrida visitamos el puerto, El Messidor, laguna Selva Triste y capilla La Asunción. Continuando el paseo y ya volviendo hacia Bariloche ascendemos el cerro Bayo, centro de ski de Villa La Angostura. Regreso al hotel.  </p>
<br><br>


<p class="destination-title">Isla Victoria y Bosque de Arrayanes</p>
<span><strong>Salidas:</strong> Diarias </span><br>
<span><strong>Duraci&oacute;n:</strong> medio día</span>
<br>
<p>Se parte desde Puerto San Carlos y/o Pañuelo en Llao Llao. Luego de una travesía de 11 km. (aproximadamente 30 minutos de navegación), se arriba a Puerto Anchorena en la Isla Victoria. Es factible realizar una excursión y caminata por la isla, recorriendo el ex vivero de coníferas. Se puede ascender por medio de una aerosilla que los llevará a la cumbre del Cerro Bella Vista a 900 mts. de altura, entre un estrecho callejón de centenarios coihues y cipreses autóctonos, regresando del cerro por los paseos azules en una serie de espectaculares miradores naturales. Luego se embarcará nuevamente para dirigirse a Puerto Quetrihue, en la península del mismo nombre, donde se puede visitar el mundialmente famoso Bosque de Arrayanes. Se trata de una formación arbórea única en el mundo con ejemplares centenarios. La corteza de este árbol es fría, enteramente lisa y de color canela. Posee unas flores blancas similares al azahar, y frutos violáceos de sabor agradable. El Bosque de Arrayanes ha sido declarado área intangible y monumento natural.  </p>
<br><br>


<p class="destination-title">Puerto Blest y Cascada Los Cántaros </p>
<span><strong>Salidas:</strong> Diarias. La excursión NO se suspende por lluvia. Consultar operatividad 01 de Enero, 01 de Mayo y 25 Diciembre. </span><br>
<span><strong>Duraci&oacute;n:</strong>10.30hs a 18.15hs</span>
<br>
<p>Paseo lacustre que nos lleva hasta el extremo oeste del Lago Nahuel Huapi, hasta adentrarnos en la cordillera y su selva valdiviana. Se visita la cascada y el lago de Los Cantaros. Opcional Lago Frías. Descripción del programa Saliendo de Puerto Pañuelo, se navega por el Brazo Blest hacia la cordillera, pasando cerca del famoso islote Centinela donde descansan los restos del Dr. Perito Francisco Pascacio Moreno. Así, usted llega a Puerto Cantaros, donde realizan una caminata hasta el Lago Los Cantaros, pasando por la Cascada Los Cántaros, y finalizando el recorrido con un imponente alerce milenario. Se regresa por el mismo camino, y tiene la opción de volver a embarcar para ir a Puerto Blest propiamente dicho, o realizar a pie el sendero de 2km aprox, por la selva valdiviana, donde la vegetación es frondosa ya que este sitio es uno de los puntos más húmedos de la Patagonia. En Puerto Blest hay un restaurant y tienen tiempo libre para almorzar. También pueden llevar su vianda. Esta excursión cuenta con Opcional a Lago Frías Partiendo de Puerto Blest recorrerá unos pocos km. en bus en medio de un paisaje boscoso hasta llegar a Puerto Alegre. Allí se embarcará en un catamarán para navegar las verdes aguas del lago Frías, observando de fondo el imponente Cerro Tronador. El trayecto finaliza en Puerto Frías, donde está el puesto de Gendarmería Nacional y el límite con Chile. Se regresa por el mismo camino. (este opcional se contrata únicamente en forma local en Bariloche el mismo día). 
Sugerencias: Llevar ropa y calzado cómodos. Cámara de fotos. No Incluye: ingreso a parques nacionales, tasa de puerto pañuelo, extensión Lago Frías ni almuerzo.
 </p>
