<p class="destination-title">Cataratas Brasileras</p>
<span><strong>Salidas:</strong> Diarias AM / PM</span><br>
<span><strong>Duraci&oacute;n:</strong> AM: 07.30hs a 15.30hs aprox. / PM: 13.30hs a 19.30hs aprox.</span>
<br>
<p>Localizadas dentro del Parque Nacional do Iguaçu, a 25 Km del centro de Foz do Iguaçu (BR). El Parque ocupa una superficie de 175.000 has. Allí se sorprenderá con el cuidado, la belleza y la enormidad del mismo que alberga innumerables especies de animales y vegetales. La naturaleza los recibirá con bienvenidas de simpáticas mariposas que se exhiben en su vuelo y lo deleitarán con sus colores. Dado que el 80% de los saltos se encuentran del lado argentino, el Parque brasileño cuenta con una sola pasarela de aproximadamente 1.000 mts., desde donde podrán disfrutar de las distintas vistas panorámicas. Finalizada la misma, frente al salto Floriano, se encuentra un mirador al que podrán acceder a través de un elevador, del que se obtiene una de las vistas más imponentes de los Saltos. Almuerzo opcional, no incluido. Para todas las excursiones recomendamos llevar repelente de insectos, zapatillas y ropa cómoda.<br>
Dutty Free Shop: Una oportunidad única de comprar las mejores marcas del mundo sin impuestos, a 10 minutos de las Cataratas. Abierto todo el año de 09:00 a 21:00 hs.<br>
Importante: Consulte la documentación necesaria para cruzar la frontera.
</p>
