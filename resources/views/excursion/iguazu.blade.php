<p class="destination-title">Saltos del Mocona</p>
<span><strong>Salidas:</strong> Jueves y Domingos</span><br>
<span><strong>Duraci&oacute;n:</strong> de 07.00hs a 19/20hs aproximadamente.</span>
<br>
<p>Los saltos del Moconá se encuentran ubicados en la localidad Misionera de El Soberbio, sobre el Río Uruguay, en frontera con Brasil. Los Saltos del Moconá (en Guaraní "El que todo lo traga") representan una irregularidad del río Uruguay, formando saltos que van desde los 5 a 20 Metros de altura, con una caída de agua que se despliega a lo largo 3 km de río, es considerado el más largo del mundo en cuanto a su longitud. Cuando el río está lo suficientemente bajo, se pueden apreciar en toda su extensión infinidad de saltos. Salidas alrededor de las 7:00 Hs. Se realiza un recorrido hacia el noroeste de la Provincia pasando por plantaciones de esencias y tabaco, ingresando a la Biosfera Yaboty hasta el Parque Provincial Moconá. Si el río lo permite, desde Piedra Bugre es posible contratar la excursión opcional de Navegación: la cual se realiza en un gomón semi rígido, con el que remontan el Río Uruguay hasta los Saltos del Moconá, y se recorre el cauce inferior de los mismos disfrutando de una vista incomparable y de un paisaje único, duración de la navegación 20 minutos. Si el río no lo permite, se visita un sendero de interpretación de la selva dentro del Parque. Almuerzo en restaurante típico del lugar. Regreso a Iguazú entre 19 y 20 Hs. 
Información Útil: Durante los meses de primavera y verano las temperaturas oscilan entre los 35° C durante el día y los 15° C por la noche. Mientras que en el otoño y el invierno, que es la estación de lluvias, de día se está cómodo con 25° C y las noches son frías con hasta 0° C. En cuanto a la ropa, se recomiendan prendas livianas y de mangas largas para el verano, botitas de trekking. Para el invierno se puede usar remera durante el día, pero durante el atardecer la temperatura desciende rápidamente y es recomendable tener a mano ropa de abrigo y campera impermeable. Llevar repelente y protector solar.

</p>
