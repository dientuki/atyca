<p class="destination-title">Ruinas Jesuíticas de San Ignacio</p>
<span><strong>Salidas:</strong> Lunes, Miércoles y Sábados (Temporada Alta) / Miércoles y Sábados (Temporada Baja)</span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día</span>
<br>
<p>Recorrido total de 480 Km. (ida y vuelta) por la Ruta Nacional Nº 12 disfrutando el paisaje de la región, pasando por pequeñas ciudades, pueblos y plantaciones se llega a las imponentes Ruinas Jesuíticas (ubicadas a 240 km. de la ciudad de Puerto Iguazú). Construidas en el siglo XVII, durante la permanencia de los Misioneros Jesuitas en esta región. La visita es acompañada por un guía local, que explicará a los visitantes la construcción, la vida y las costumbres de los aborígenes guaraníes y la catequización de los mismos por los Jesuitas.
En el trayecto se visita las Minas de Piedras Preciosas en la ciudad de Wanda. 

</p>
<br><br>





<p class="destination-title">Minas de Piedras Preciosas “Wanda”</p>
<span><strong>Salidas:</strong> Diarias, PM: 15hs.</span><br>
<span><strong>Duraci&oacute;n:</strong> Medio día</span>
<br>
<p>Representa un recorrido de 40 Km. desde la ciudad de Puerto Iguazú hasta la Ciudad de Colonia Wanda, sobre la ruta Nacional Nº 12. El yacimiento de las minas de Piedras Preciosas resulta un atractivo singular, con la particularidad de encontrarse a cielo abierto sobre el caudaloso río Paraná, con muestras de ágatas, cuarzos, cristal de roca, amatista y topacio. Acompaña el paisaje, cubierto con extensas plantaciones de pinos, yerba mate y té, entre otros, pilares de la economía regional.

</p>
