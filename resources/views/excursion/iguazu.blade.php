<p class="destination-title">Gran Aventura</p>
<span><strong>Salidas:</strong> Diarias</span><br>
<span><strong>Duraci&oacute;n:</strong> 1hs 20min aproximadamente</span>
<br>
<p>La Gran Aventura conjuga la esencia del Parque Nacional Iguazú: la selva y las Cataratas en un único recorrido.
Partiendo de la Central Operativa o desde el Centro de Visitantes, ingresarás a la selva por el estrecho Sendero Yacaratiá en unidades diseñadas para este particular ambiente. Guías bilingües (español/inglés) ayudarán a interpretar las riquezas naturales y culturales del lugar. 
Este recorrido vehicular de 5 km culmina en la base selvática Puerto Macuco. Luego de descender 100 metros llegarás al muelle donde podrás abordar las lanchas con las que remontarás 6 km el cañán del río Iguazú Inferior rumbo al área de cascadas disfrutando de los caudalosos rápidos, hasta la base del Salto Tres Mosqueteros avistando el conjunto de saltos brasileños y argentinos, con la Garganta del Diablo coronando la vista del cañán. Posteriormente, alcanzarás el máximo punto de emoción al enfrentar el incomparable Salto San Martín, segundo en cuanto a dimensión y el mayor al que una lancha puede aproximarse. Para finalizar desembarcaremos en el muelle ubicado frente a la Isla San Martín desde donde podrás reintegrarte al sistema de pasarelas, a través del Circuito Inferior. La Gran Aventura puede realizarse en sentido contrario. Es decir, desde el muelle ubicado frente a la Isla San Martín hasta el Centro de Visitantes. Dificultad: 150 metros de escaleras y 2 Km de aguas rápidas.
Recomendaciones: Proteger todos los objetos personales colocándolos dentro de las bolsas impermeables especialmente diseñadas y provistas por la empresa prestadora, caso contrario se mojarán durante la aproximación a los saltos. 
Restricciones: No podrán realizar este paseo, por cuestiones de seguridad, las personas que respondan a los siguientes rasgos o dolencias: menores de 12 años, mujeres embarazadas, personas con osteoporosis, problemas de columna o similares, con antecedentes cardíacos o trastornos neurológicos, con enfermedades pulmonares crónicas y toda persona con discapacidades físicas, cognitivas y/o sensoriales que no pueda responder ante una eventual emergencia en las aguas rápidas del río Iguazú inferior.
Para este servicio es obligatorio presentar o exhibir Documento de Identidad que acredite la edad mínima para embarcar (12 años).

</p>
