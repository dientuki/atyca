      <h2 class="destination-subtitle">Avenida Corrientes</h2>

      <p>Es una de las calles más representativas de Buenos Aires y una de las más queridas por los porteños. Corrientes
        es la avenida de las librerías, los cafés, los teatros y las pizzerías.</p>

      <p>Corrientes nace en el barrio de Puerto Madero y termina en el de Chacarita tras 70 cuadras de recorrido. Entre
        1910 y 1938 se realizó una gran obra que ensanchó la calle y la transformó en avenida. Su nombre, como el de las
        avenidas vecinas Córdoba y Santa Fe, es el de una provincia argentina. Atraviesa el centro porteño y las zonas
        de
        Once, Abasto, Almagro y Villa Crespo, partes muy típicas de Buenos Aires y con fuerte relación histórica con el
        tango.</p>

      <p>Una asociación de amigos de Corrientes instaló en algunas de sus esquinas escenas tangueras compuestas por
        figuras esculpidas, como la que se ve en la esquina con la calle Drago.</p>

      <p>La mayoría de las pizzerías más tradicionales están sobre Corrientes, especialmente en el centro: Güerrín, Los
        Inmortales, Banchero, Las Cuartetas y el Palacio de la Pizza, pero también en Villa Crespo, Almagro y
        Chacarita.</p>

      <p>Alrededor del Obelisco, entre la avenida Callao y la calle Esmeralda (en el Centro), Corrientes tiene la mayor
        concentración de librerías y teatros de toda la ciudad. Prácticamente todas las salas teatrales más importantes
        de
        Buenos Aires están en este tramo, como el Maipo, el Gran Rex, el Ópera, el Avenida, el San Martín, el Astral, el
        Lola Membrives, el Multiteatro, el Metropolitan y el Alvear, entre otros.</p>

      <p>Las librerías de Corrientes suelen permanecer abiertas hasta después de la medianoche, especialmente los fines
        de
        semana, y las más grandes tienen bar y salas de espectáculos.</p>

      <p>Este trecho de la avenida también concentra una gran cantidad de los bares y cafés más famosos de Buenos Aires,
        como La Giralda, La Paz o El Vesuvio.</p>

      <h2 class="destination-subtitle">Barrio Chino</h2>

      <p>Surgió alrededor de la década de 1980 con el establecimiento en el barrio de familias de inmigrantes
        orientales,
        principalmente de Taiwán.</p>

      <p>Es visitado por su oferta gastronómica y cultural: restaurantes, supermercados de productos orientales,
        excelentes pescaderías y verdulerías, locales de manga y animé (historieta y video japoneses), objetos de
        decoración, etc. En la zona se encuentra, entre otras instituciones religiosas, uno de los pocos templos
        budistas
        de la ciudad (Templo Chong Kuan, Montañeses 2175).</p>

      <h2 class="destination-subtitle">Bosques de Palermo</h2>

      <p>Los bosques de Palermo, oficialmente Parque Tres de Febrero, es una zona verde de 25 ha en el barrio de
        Palermo,
        en Buenos Aires, Argentina. Localizados entre las avenidas Casares y Libertador, se destacan por sus arboledas,
        lagos y rosedales, en cuyo diseño participó el arquitecto Carlos Thays.</p>

      <p>Una importante cantidad de gente recorre los bosques día a día, ya sea a pie o en bicicleta, y la afluencia se
        incrementa notablemente en los fines de semana. También se pueden realizar paseos en bote por los tres lagos
        artificiales que se existen en la zona.</p>

      <p>En esta parte de la ciudad se encuentra el Planetario, cuya edificación es muy particular, puesto que se trata
        de
        una esfera sostenida por tres arcos. En las proyecciones que se llevan a cabo en su interior, se pueden observar
        los planetas y las estrellas de la galaxia.</p>

      <p>También están localizados en esta zona de la ciudad el Jardín Japonés, el Jardín Zoológico y el Jardín
        Botánico,
        que terminan en Plaza Italia, que es el centro del barrio de Palermo, con su tradicional avenida Santa Fe.</p>

      <h2 class="destination-subtitle">Café Tortoni</h2>

      <p>Se sabe que fue inaugurado en 1858, pero existen dos versiones respecto al porqué de su nombre: una de ellas
        dice
        que un inmigrante francés de apellido Touan, lo había establecido en la esquina de Rivadavia y Esmeralda,
        nombrándolo Tortoni porque así se llamaba un establecimiento del Boulevard des Italiens donde se reunía la elite
        de la cultura parisina del siglo XIX. La otra versión, afirma que fue un tal Oreste Tortoni quien habría
        establecido el café sobre la calle Defensa al 200. Uno de los últimos dueños del Tortoni, el señor Fanego, está
        a
        favor de la primera versión y afirma que la segunda nació de un error de un articulista de un folleto
        publicitario
        de uno de los proveedores, que inventó al tal Oreste Tortoni. Sin embargo Enrique Puccia, historiador de Buenos
        Aires, descubrió que efectivamente existió una guía de la ciudad donde aparece el Café Tortoni en Defensa al
        200.
        No obstante, el Gran Mapa Mercantil de la Ciudad de Buenos Aires, editado en 1870, por Rodolfo Kratzenstein lo
        ubica en Rivadavia y Esmeralda con Monsieur Touan como propietario.</p>

      <p>Lo cierto es que en 1880 fue trasladado a su lugar actual, donde anteriormente se encontraba el denominado
        Templo
        Escocés de Buenos Aires, pero su entrada era por la calle Rivadavia. A partir de 1898 se tuvo su entrada
        principal
        por Avenida de Mayo, (que había sido inaugurada en 1894), y la fachada fue realizada en por el arquitecto
        Alejandro Christophersen. A finales del siglo XIX el café es comprado por otro francés, Celestino Curutchet, que
        habitaba en los altos del café.</p>

      <h2 class="destination-subtitle">Calle Lanin</h2>

      <p>El pintor Marino Santa María nació en 1949 en el barrio de Barracas. Su padre —Marino Pérsico— fue un pintor y
        ceramista reconocido a mediados del siglo XX.</p>

      <p>Santa María, en la década de 1990, cubrió el frente de su taller, en Lanín 33, con una de sus obras. La idea
        gustó entre los vecinos, que de a poco le fueron pidiendo que también pintara las fachadas de sus casas. Por
        iniciativa del artista, se hizo un convenio — del que participaron empresas e instituciones como el gobierno
        porteño, la Unesco y el Museo Nacional de Bellas Artes —para financiar el proyecto de pintar 35 casas a lo largo
        de las tres cuadras de la calle Lanín.</p>

      <p>La obra demandó dos años y el trabajo de alrededor de 20 personas entre albañiles y pintores. Las 35 fachadas
        fueron lavadas, selladas, revocadas y finalmente pintadas con esmalte sintético.</p>

      <h2 class="destination-subtitle">La Boca</h2>

      <p>La Boca es un barrio de Buenos Aires, en Argentina. Está situado en el límite sudeste de la ciudad. Su nombre
        se
        debe a que se ubica en la desembocadura del Riachuelo en el Río de la Plata.</p>
      En épocas de la Colonia española La Boca era una zona de barracones para los esclavos negros. En el período
      independiente funcionaron allí saladeros (cobertizos en que se salaba la carne vacuna para la exportación) y
      curtiembres de cuero. A fines del siglo XIX comenzó a ser habitado por inmigrantes italianos, principalmente
      genoveses, que le dieron su fisonomía actual.
      <p>La Boca de 1870 tenía ya una fisonomía característica y en 1895 era la segunda sección de la Capital. Sobre una
        población de 38.000 habitantes, 17.000 eran argentinos, 14.000 italianos, 2.500 españoles y el resto de otras
        nacionalidades.</p>

      <p>A los residentes de La Boca y por extensión a su equipo de fútbol, el popular Boca Juniors, se los conoce aún
        como Xeneizes (genoveses, en su propio dialecto). Los inmigrantes construyeron casas de chapas de metal
        acanaladas, montadas muchas veces sobre pilotes o cimientos altos debido a las frecuentes inundaciones, y
        pintadas
        con colores brillantes.</p>

      <p>Zona portuaria y proletaria, el mayor exponente artístico del barrio fue Benito Quinquela Martín, un pintor que
        reflejó las tareas del puerto y la vida cotidiana de su gente en estilo neoimpresionista. También el primer
        diputado socialista de América fue elegido por ese barrio en 1905, cuando votó a Alfredo Palacios para que los
        representara en el Congreso.</p>

      <h2 class="destination-subtitle">Caminito</h2>

      <p>La vecindad del Riachuelo es uno de los sectores de la ciudad más visitados por los turistas por estar muy
        vinculado a la mitología del tango. La Vuelta de Rocha, donde el Riachuelo hace una amplia curva, es uno de sus
        lugares más caracerísticos, como Caminito, una callejuela inmortalizada por el tango del mismo nombre, de Juan
        de
        Dios Filiberto. Allí se venden pinturas, souveniers y artesanías. Los domingos hay parejas de tango que bailan
        sobre su empedrado.</p>

      <p>La ribera fue convertida en paseo y una obra de ingeniería hidráulica ha conjurado la maldición de las
        inundaciones. El estadio de Boca Juniors es considerado un monumento por los xeneizes. Llamado La Bombonera por
        sus gradas elevadas que le dan forma de caja, el espectáculo de un partido de fútbol resulta allí inolvidable
        por
        el reconocido fervor de los hinchas (aficionados) de Boca y su carnaval de luces de bengala, papeles de colores
        y
        bombas de estruendo.</p>

      <p>El Riachuelo muestra un paisaje de barcos abandonados, el esqueleto de un antiguo puente de hierro y las
        siluetas
        de silos y edificaciones industriales en la otra orilla. Este paisaje áspero, sólo mitigado por el colorido de
        las
        casas, inspiró el tango Nieblas del Riachuelo, del músico Juan Carlos Cobián y el poeta Enrique Cadícamo.</p>
      Además del Museo Quinquela Martín, en la Boca se encuentra la sala de exposiciones de la Fundación Proa, que
      realiza
      muestras de pintura contemporánea argentina y latinoamericana.
      <h2 class="destination-subtitle">Casa de la Cultura</h2>

      <p>Se construyó para el diario La Prensa. Hoy lo ocupa la Casa de Cultura del Gobierno de Buenos Aires. Es uno de
        los edificios del siglo XIX más lujosos de la ciudad.</p>

      <p>La obra la encargó José C. Paz, fundador de La Prensa, a un estudio de arquitectos parisino. El proyecto
        contemplaba sectores para cada paso del proceso de confección de un diario, incluyendo la impresión y
        distribución. El diseño fue completado y ejecutado en Buenos Aires por los ingenieros Alberto Gainza y Carlos
        Agote.</p>

      <p>El edificio —de seis pisos y dos subsuelos— se estructura en torno a un patio central. Se lo dotó de la última
        tecnología de la época: un sistema de comunicación interna de tubos neumáticos, teléfonos y emisores y
        receptores
        de cables de noticias.</p>

      <p>El primer ejemplar de La Prensa apareció en 1869. En la primera mitad del siglo XX, La Prensa llegó a ser el
        diario más popular del país, con corresponsales en todas las provincias, en todas las capitales del continente,
        en
        la mayoría de los países europeos y en otras naciones del mundo.</p>

      <p>Tras sufrir clausuras y crisis financieras, en 1992 el diario se mudó a un edificio de Puerto Madero, y en 1993
        el gobierno de Buenos Aires instaló en el lugar la Casa de la Cultura (que organiza espectáculos, cursos y
        talleres) y otras oficinas gubernamentales.</p>

      <h2 class="destination-subtitle">Cementerio de la Recoleta</h2>

      <p>El Cementerio de La Recoleta es un famoso cementerio ubicado en el barrio del mismo nombre de Buenos Aires
        (Argentina). Los jardines que rodean al cementerio constituyen un área de esparcimiento popular. Por otra parte,
        Recoleta es uno de los barrios más distinguidos de la ciudad y el cementerio hace honor a esa
        característica.</p>

      <p>Los frailes de la orden de los recoletos descalzos llegaron a esta zona, entonces en las afueras de Buenos
        Aires,
        a principios del siglo XVIII. Construyeron en el lugar un convento y una iglesia, que colocaron bajo la
        advocación
        de la Virgen del Pilar (esta iglesia, iglesia Nuestra Señora del Pilar, construida en 1732, aún existe y fue
        nombrada monumento histórico nacional. Los lugareños terminaron denominando a la iglesia de los recoletos o
        simplemente Recoleta, nombre que se extendió a denominar a toda la zona. Cuando la orden fue disuelta en 1822,
        la
        huerta del convento fue convertida en un cementerio público, el primero del que dispuso la ciudad de Buenos
        Aires.</p>

      <p>Durante la década de 1870, como consecuencia de la epidemia de fiebre amarilla que asoló la ciudad, muchos
        porteños de clase alta abandonaron el barrio de San Telmo y se mudaron a la parte norte de la ciudad, a
        Recoleta.
        Al convertirse en barrio de clase alta, el cementerio se convirtió en el último reposo de las familias de mayor
        prestigio y poder de Buenos Aires.</p>

      <p>La entrada al cementerio se realiza a través de un pórtico neoclásico con altas columnas. El cementerio alberga
        varios mausoleos de mármol, decorados con estatuas, en una amplia variedad de estilos arquitectónicos. Se halla
        organizado en manzanas, con amplias avenidas arboladas que dan a callejones laterales donde se alinean los
        mausoleos y bóvedas. Existe una amplia rotonda central de donde parten las avenidas principales, con una
        escultura
        de Cristo realizada por el escultor Zonza Briano.</p>

      <p>Cada mausoleo presenta el nombre de la familia labrado en la fachada; se agregan al frente placas de bronce
        para
        los miembros individuales, incluyendo varios presidentes de Argentina.</p>

      <p>Jorge Luis Borges fantaseó en un poema con ser enterrado en este lugar, pero finalmente no fue así. Él duerme
        su
        sueño eterno en la ciudad de Ginebra, Suiza.</p>

      <h2 class="destination-subtitle">Edificio Kavanagh</h2>

      <p>La millonaria porteña Corina Kavanagh encargó en 1934 un rascacielos a uno de los estudios de arquitectura más
        prestigiosos de la próspera Buenos Aires de comienzos del siglo XX. La señora Kavanagh vendió dos de sus
        estancias
        para financiar la construcción. El edificio se erigió en 14 meses.</p>

      <p>Es una torre escalonada de hormigón armado de 120 metros de altura, 32 pisos y 105 viviendas. Se levanta frente
        a
        la Plaza San Martín. Cuando se inauguró fue el edificio más alto de Latinoamérica y la mayor estructura en el
        mundo de hormigón armado. El estilo es racionalista, caracterizado por la austeridad de las líneas, la carencia
        de
        ornamentos externos y los grandes volúmenes prismáticos.</p>

      <p>Para asegurar la calidad de la obra no se pusieron límites al presupuesto. El Kavanagh, proyectado como un
        edificio de departamentos de lujo, ganó numerosos premios nacionales e internacionales.</p>

      <p>Corina Kavanagh —que tenía 39 años cuando encargó el edificio— se reservó para sí el departamento del piso 14,
        el
        único que ocupa toda una planta.</p>

      <h2 class="destination-subtitle">Estadio de River Plate</h2>

      <p>River Plate es uno de dos los clubes de fútbol más populares del país junto con su rival, Boca Juniors. La
        cancha
        de River es conocida popularmente como "El Monumental"; fue uno de los primeros grandes estadios de
        Sudamérica.</p>

      <p>La piedra fundamental se colocó en 1935 en terrenos que en esos tiempos provocaron polémica por su cercanía con
        el río y el peligro de las inundaciones. Tres años más tarde se inauguró con lugar para 70.000 espectadores.
        Sucesivas reformas lo ampliaron hasta la capacidad actual de 76.609, la mayor de los estadios en todo el
        país.</p>

      <p>En el estadio suele jugar la selección nacional de fútbol y además es sede de espectáculos musicales
        multitudinarios.</p>

      <p>Los partidos del torneo de 1ª división se juegan los viernes, sábados y (la mayoría) los domingos.</p>

      <p>River se fundó en 1901 en el barrio de La Boca; en la década de 1920 el club mudó su sede varias veces, hasta
        que
        se instaló en el límite de los barrios de Núñez y Belgrano.</p>

      <h2 class="destination-subtitle">Feria de Plaza Dorrego</h2>

      <p>Plaza Dorrego es el nombre con que popularmente se designa a la Plaza Coronel Manuel Dorrego. Se encuentra en
        el
        corazón del barrio de San Telmo, en Buenos Aires, Argentina y ocupa un cuarto de manzana. Está delimitada por
        las
        calles Humberto I° y Defensa y por dos cortadas: Una paralela a Humberto I° llamada Bethlem, y otra paralela a
        Defensa de nombre Anselmo Aieta.</p>

      <p>Antiguamente a este lugar se lo denominaba Hueco del Alto o Alto de las carretas, pues era allí donde los
        carros
        tirados por bueyes se detenían antes de cruzar el arroyo Tercero del Sur (hoy calle Defensa y Pasaje San
        Lorenzo)
        en su trayecto al centro de la ciudad. Posteriormente su nombre cambió por Alto de San Pedro, y luego Plaza del
        Comercio (1822), entre otros. En 1900, el nombre de la plaza cambió por el actual.</p>

      <p>Actualmente constituye uno de los principales paseos turísticos peatonales de Buenos Aires. A su alrededor se
        encuentran cafés, bares y pubs con sus mesas sobre la plaza; y también casas de antigüedades y artesanías.
        Abundan
        los shows de músicos y baile callejero, incluyendo exhibiciones de tango. Los domingos tiene lugar la Feria de
        San
        Telmo, durante la cual la plaza y las calles aledañas son ocupadas por puestos de antigüedades y curiosidades
        que
        son recorridos por numerosos turistas tanto del país como del extranjero y que contribuyen a darle a esa zona
        del
        barrio una fisonomía muy particular.</p>

      <p>En octubre de 1978 la plaza fue declarada Lugar Histórico según decreto 2.272 debido a que en su sitio el
        pueblo
        de Buenos Aires adhirió a la Independencia en 1816. Efectivamente: La noticia de la proclamación de la
        Declaración
        de la Independencia en el Congreso de Tucumán del 9 de julio de 1816 se conoció en Buenos Aires dos meses
        después,
        y el 13 de septiembre de ese año se llevó a cabo en horas de la mañana un acto en la Plaza de Mayo. Por la tarde
        el acto siguió en la entonces Plaza del Comercio con una ceremonia en la que estuvieron presentes el Director
        Supremo, Juan Martín de Pueyrredón, el presidente del Cabildo y las autoridades eclesiásticas. Una placa en
        mayólica sobre la pared de la calle Bethlem recuerda este acontecimiento.</p>

      <h2 class="destination-subtitle">Galerías Pacifico</h2>

      <p>Es una de las construcciones más fastuosas de la ciudad. Se ubica sobre la calle peatonal Florida, uno de los
        paseos favoritos de los visitantes.</p>

      <p>El edificio se hizo para alojar las tiendas Au Bon Marché Argentina, pero por problemas económicos, éstas nunca
        llegaron a inaugurarse y se instalaron en cambio varios comercios más pequeños. En diciembre de 1896, las
        galerías
        se transformaron en la primera sede del Museo Nacional de Bellas Artes; en 1908, el Ferrocarril Buenos Aires al
        Pacífico adquirió parte del predio para sus oficinas y se lo comenzó a llamar Edificio Pacífico.</p>

      <p>En 1945 fue remodelado por los arquitectos Jorge Aslan y Héctor Ezcurra. La obra separó el sector de oficinas
        del
        comercial, y se coronó con una cúpula de 450 metros cuadrados cubierta por murales de algunos de los artistas
        más
        prestigiosos del momento: Spilimbergo, Berni, Castagnino, Colmeiro y Urruchúa. Los doce paneles pintados
        conforman
        uno de los conjuntos murales más importante de Buenos Aires. El fresco de Antonio Berni (1905-1981) es uno de
        los
        más reconocidos.</p>

      <p>Luego de años de abandono, las galerías fueron reformadas y reinauguradas en 1990 como centro comercial.
        Entonces
        se agregaron otros cuatro murales de: Rómulo Macció, Josefina Robirosa, Guillermo Roux y Carlos Alonso.</p>

      <p>En el edificio funcionan el Centro Cultural Jorge Luis Borges y el Estudio de danza Julio Boca.</p>

      <h2 class="destination-subtitle">Malba</h2>

      <p>Es uno de los museos más modernos de Buenos Aires. Aloja la colección Costantini: más de 200 piezas (pinturas,
        esculturas, grabados, fotografías y objetos) de artistas latinoamericanos del siglo XX.</p>

      <p>La colección de Eduardo Costantini se exhibió completa en el Muse Nacional de Bellas Artes en 1995; tres años
        más
        tarde, el empresario llamó a concurso internacional para diseñar un museo que la alojara en forma
        permanente.</p>

      <p>El edificio está revestido de piedra caliza y tiene grandes superficies de vidrio y acero.</p>

      <p>Entre muchas, hay obras de Frida Kahlo, Wifredo Lam, Roberto Matta, Diego Rivera, Joaquín Torres-García,
        Antonio
        Berni, Emiliano Di Cavalcanti, Jorge de la Vega, Tarsila do Amaral, Pedro Figari, Lygia Clark, Helio Oiticica,
        Liliana Porter, Guillermo Kuitca y José Bedia Valdés.</p>

      <p>En el museo funcionan una sala de cine, un café, una tienda de regalos y una librería.</p>

      <h2 class="destination-subtitle">Manzana de las Luces – Museo San Ignacio</h2>

      <p>Contiene algunas de las construcciones más antiguas que quedan en Buenos Aires, como San Ignacio, la iglesia
        más
        vieja de la ciudad.</p>

      <p>El nombre -propuesto por el diario Argos en 1821- hace referencia a los establecimientos educativos y
        culturales
        que funcionan en la Manzana desde hace siglos.</p>

      <p>Su trazado está previsto en los primeros planos de Buenos Aires, de 1580.</p>

      <p>En 1661 fue cedida a los jesuitas, que en la manzana levantaron una iglesia, un colegio, una escuela de
        primeras
        letras, una farmacia, una imprenta y la oficina general de sus misiones.</p>

      <p>La Iglesia de San Ignacio (Bolívar y Alsina) se comenzó en 1686, sobre los muros de adobe de un templo que ya
        existía en el lugar. Se inauguró en 1722; la obra estuvo a cargo de los arquitectos jesuitas Krauss, Bianchi y
        Prímolo.</p>

      <p>A fines del siglo XVIII funcionó como catedral de la ciudad y se convirtió en cuartel militar durante la
        resistencia a la invasión inglesa de 1806.</p>

      <p>Después de la expulsión de la orden, en 1767, en el lugar se instalaron la escuela de medicina; la biblioteca
        nacional; el primer museo nacional; la Universidad de Buenos Aires; el primer teatro porteño; la academia de
        jurisprudencia; las facultades de arquitectura y ciencias exactas.</p>

      <p>Debajo de la Manzana corre una red de túneles de los siglos XVII y XVIII que unían edificios e iglesias del
        centro con el fuerte de Buenos Aires. Se cree que sirvieron para defensa de la ciudad y para el contrabando.</p>

      <h2 class="destination-subtitle">Museo de Arte Español Enrique Larreta</h2>

      <p>Está frente a la Plaza Belgrano y a la iglesia redonda (parroquia de la Inmaculada Concepción), inaugurada en
        1878. El museo está rodeado por el jardín andaluz más hermoso de Buenos Aires.</p>

      <p>En 1882, Ernesto Bunge —un arquitecto de la corriente academicista— construyó una casa de veraneo en Belgrano
        sobre un terreno de más de 7.000 metros cuadrados. Para proyectar la fachada, Bunge se inspiró en el edificio de
        la Aduana Vieja.</p>

      <p>En 1903 la dueña era Josefina Anchorena, quien se casó con Enrique Larreta (1873-1961). Larreta estudió leyes,
        se
        desempeñó como embajador argentino en Francia, vivió en varios países europeos y escribió novelas, poemas y
        obras
        de teatro. Muchos de sus libros tratan temas históricos y su estilo se considera modernista. Era un estudioso de
        España, sobre todo del Siglo de Oro.</p>

      <p>Tras la muerte del escritor, la familia donó parte de su colección de arte español a la municipalidad, que a su
        vez compró la casona para alojarla.</p>

      <p>Entre los objetos de la muestra permanente hay esculturas y pinturas, sobre todo del renacimiento, del
        manierismo
        y del barroco españoles; muebles; cerámica; armaduras y armas del siglo XVI; libros antiguos; metalería, como
        braseros, candelabros, platos, etc.</p>

      <h2 class="destination-subtitle">Museo de Carlos Gardel</h2>

      <p>Carlos Gardel fue uno de los artistas argentinos más populares de la historia, tanto local como
        internacionalmente y aún hoy es un símbolo para los porteños. El museo se aloja en la última casa que el %p
        cantante habitó en Buenos Aires.</p>

      <p>Gardel nació en 1890 en. Se crió en el barrio del Abasto, donde en la década de 1910 comenzó a cantar canciones
        camperas y milongas; más adelante se convirtió en pionero del tango-canción. Su forma de cantar sigue siendo
        canónica.</p>

      <p>Gardel grabó discos y filmó películas en Latinoamérica, Europa y Estados Unidos; también compuso temas que se
        convirtieron en clásicos, como "El día que me quieras", "Arrabal amargo", "Volver"y "Cuesta abajo.</p>

      <p>Murió en un accidente de aviación en Medellín, Colombia, en 1935. Fue enterrado en el cementerio de Chacarita,
        donde su tumba puede visitarse.</p>

      <p>Compró la casa que hoy alberga el museo en 1927 para su madre Berta, con quien la compartió hasta 1933, cuando
        abandonó el país por última vez. La construcción (una casa chorizo) es típica de la primera mitad del siglo XX.
        El
        museo (dependiente del gobierno porteño) exhibe objetos personales de Gardel y ejemplos de su obra.</p>

      <p>Museo de Arte Hispano Fernández Blanco</p>

      <p>En la década de 1920, el arquitecto Martín Noel construyó para su residencia la casona que hoy alberga el
        museo.
        El edificio es de estilo neocolonial, que combina elementos españoles y de las colonias americanas.</p>

      <p>En 1936, la municipalidad porteña adquirió la casa junto con la colección de arte de Noel, base del Museo de
        Arte
        Colonial que se inauguró en el lugar un año más tarde. En 1943, Isaac Fernández Blanco donó su colección al
        museo,
        que en 1947 tomó su nombre actual.</p>

      <p>Fernández Blanco tenía el título de ingeniero pero era un gran aficionado al violín, y la fortuna familiar le
        permitió formar una importante colección de instrumentos de cuerda. Más adelante, extendió su interés a objetos
        de
        la cultura hispanoamericana: platería, imágenes religiosas, pintura, mobiliario, libros y documentos. El
        patrimonio del museo se enriqueció con las donaciones de Celina González Garaño en 1963 (principalmente platería
        y
        pintura) y de su hermano Alfredo en 1991.</p>

      <p>El patrimonio del Fernández Blanco se considera uno de los más importantes de arte hispanoamericano,
        principalmente en platería peruana, altoperuana y rioplatense; en pintura altoperuana y cuzqueña; en imaginería
        quiteña y jesuítica; en mobiliario lusobrasileño y en artes decorativas.</p>

      <p>Entre las colecciones menores se destaca la de peinetas de carey o asta que las mujeres usaban en el Río de la
        Plata en la primera mitad del siglo XIX.</p>

      <h2 class="destination-subtitle">Museo Nacional de Bellas Artes</h2>

      <p>Es el museo de bellas artes más importantes del país, y está entre los principales del mundo. Su colección de
        arte argentino es la mayor que existe.</p>

      <p>A finales del siglo XIX, los artistas argentinos formados en Europa (especialmente en Italia, Francia y España)
        que volvían al país comenzaron a exponer sus obras y a organizar muestras. En 1885 se hizo en Buenos Aires la
        primera muestra individual de un pintor nacional, con 29 telas de Cándido López. En 1893 se organizó el primer
        salón nacional de pintura. Hasta 1897 no se fundió una estatua en el país: el monumento a Falucho, de Correa
        Morales, que hoy puede verse en la esquina de Santa Fe y Luis María Campos.</p>

      <p>En 1896 se inauguró el MNBA. Su primera sede, alquilada, fue el edificio en Córdoba y Florida donde actualmente
        están las Galerías Pacífico. El crecimiento del museo obligó a que se mudara dos veces hasta que en 1933 se
        inauguró la sede actual, en el antiguo edificio de la Casa de Bombas (donde se filtraba el agua de río), de
        1870.
        El arquitecto Alejandro Bustillo se encargó de la reforma y diseñó ambientes espaciosos, bien iluminados y
        austeros. Distintas salas y pabellones se agregaron al edificio durante las décadas de 1960 y 1980.</p>

      <p>En el patrimonio del MNBA, del arte internacional, se destacan obras de El Greco, Goya, Rodin, Renoir, Degas,
        Cézanne, Picasso. Entre los pintores argentinos están Cándido López, Spilimbergo, Pueyrredón, Fader, Quinquela
        Martín, Xul Solar, Berni, Alonso, de la Vega, Gorriarena, Seguí, y muchos más.</p>

      <p>El museo cuenta también con una sala de fotografía, una de arte andino precolombino, dos terrazas de esculturas
        y
        una biblioteca de más de 150.000 ejemplares.</p>

      <h2 class="destination-subtitle">Obelisco</h2>

      <p>El obelisco de Buenos Aires es un Monumento Histórico Nacional ícono de la ciudad de Buenos Aires, Argentina.
        Emplazado en la Plaza de la República, en la intersección de las avenidas Corrientes y 9 de Julio, fue
        construido
        con motivo del cuarto centenario de la primera fundación de la ciudad.</p>

      <p>Tiene una sola puerta de entrada (en el lado oeste) y en su cúspide hay cuatro ventanas, a las que sólo se
        puede
        llegar por una escalera recta de 206 escalones con 7 descansos cada 8 m y uno a 6 m.</p>

      <p>En donde se emplaza el Obelisco, previamente, debió demolerse la iglesia dedicada a San Nicolás de Bari. En
        ella
        se había izado oficialmente por primera vez la Bandera Argentina dentro de la ciudad de Buenos Aires en 1812:
        dicha circunstancia se recuerda en una de las inscripciones del lado norte.</p>

      <p>El Obelisco es frecuentemente utilizado como punto de reunión de diversas manifestaciones, las que abarcan
        también las avenidas 9 de Julio y Corrientes que lo rodean, hecho similar al que ocurre en Plaza de Mayo.</p>

      <p>El 20 de septiembre de 2007, conmemorando los 150 años de las relaciones bilaterales entre Argentina y
        Alemania,
        el obelisco amaneció cubierto con una tela de 60 m² con los colores de las banderas de esos países. Además se
        ajustó un nuevo sistema complementario de iluminación que fue donado por la embajada de Alemania y empresas de
        ese
        país que apoyaron la celebración.</p>

      <h2 class="destination-subtitle">Palacio Barolo</h2>

      <p>El Palacio Barolo es un edificio de oficinas ubicado sobre la Avenida de Mayo, en el barrio de Monserrat, en
        Buenos Aires, Argentina. Cuando fue construido era el edificio más alto de la ciudad y de América del Sur. Este
        edificio tiene su hermano gemelo en estilo, pero de mayor altura, construido por el mismo arquitecto en
        Montevideo, el Palacio Salvo.</p>

      <p>El arquitecto italiano Mario Palanti construyó este palacio a pedido del empresario textil Luis Barolo. Entre
        1923, fecha de la inauguración, y 1935 fue el edificio más alto de la ciudad, habiendo sido desplazado del
        primer
        puesto cuando se construyó el Kavanagh. Su altura es equiparable a la de un vigésimocuarto piso.</p>

      <p>Su propietario tuvo en mente construirlo para utilizar tres pisos y luego alquilar los demás. Hoy por hoy,
        funciona como un edificio de oficinas. Su faro posee 300.000 bujías en condiciones de funcionar. El edificio
        está
        lleno de analogías y referencias a la Divina Comedia, motivadas por la admiración que su creador profesaba por
        Alighieri. La división general del Palacio sigue la estructura de la Divina Comedia, es así que el Palacio tiene
        tres partes, al igual que la obra de Dante: Infierno, Purgatorio y Cielo. Además, la división estructural sigue
        en
        todo una correspondencia exacta y el Faro representa a los "Nueve Coros Angelicales". La estructura tiene 100
        metros, mismo número de cantos que tiene la Divina Comedia y 22 son los pisos, al igual que las estrofas de los
        versos de esta obra.</p>

      <h2 class="destination-subtitle">Parque Lezama</h2>

      <p>En el límite sur de San Telmo, es uno de los grandes parques porteños. Está emplazado en una de las pocas
        barrancas naturales de la ciudad.</p>

      <p>Algunos historiadores creen que las tierras que hoy ocupa el parque fueron el sitio de la primera fundación de
        Buenos Aires, en 1536.</p>

      <p>En el siglo XIX, los terrenos pasaron a manos de un norteamericano y después de un inglés, por lo que los
        vecinos
        bautizaron el lugar como "la quinta de los ingleses".</p>

      <p>En 1857, la propiedad fue comprada por el comerciante salteño Gregorio Lezama, que importó árboles y plantas
        exóticas y contrató a paisajistas europeos para que diseñaran uno de los jardines privados más lujosos de la
        época. Su viuda, Ángela Álzaga, vendió los terrenos a la comuna en 1894 por un precio muy bajo, con la condición
        de que se convirtieran en un parque público con el nombre de su marido.</p>

      <p>El parque tiene varias esculturas (una loba romana, por ejemplo), monumentos (como el Monumento a la
        cordialidad
        internacional, sobre Av. Martín García), un anfiteatro, un mirador y una fuente. Sobre la calle Defensa, en la
        casona del siglo XIX que habitó Lezama, se estableció en 1897 el Museo Histórico Nacional, todavía en
        funcionamiento.</p>

      <p>Frente al parque se levanta la Iglesia Ortodoxa Rusa (Brasil 315), reconocible por su estilo moscovita y sus
        cúpulas azules.</p>

      <p>En la esquina de Brasil y Defensa hay dos de los cafés más famosos de la ciudad: El Británico e Hipopótamo.</p>

      <h2 class="destination-subtitle">Plaza de Mayo</h2>

      <p>Es la plaza más vieja de Buenos Aires. Su ubicación fue determinada en la segunda fundación de la ciudad, en
        1580. Está rodeada por edificios históricos y gubernamentales: el Cabildo, la Catedral, la Casa Rosada (sede del
        gobierno nacional), el Palacio de Gobierno porteño, bancos y ministerios.</p>

      <p>El 11 de junio de 1580, el español Juan de Garay realizó la ceremonia de fundación de la Ciudad de la Santísima
        Trinidad y Puerto de Santa María del Buen Ayre en las tierras que hoy ocupa la Plaza de Mayo. La aldea se
        levantó
        a su alrededor.</p>

      <p>Hasta mediados del siglo XVII su superficie era la mitad de la actual; en 1884 se demolió una recova que la
        dividía en dos y recibió su denominación moderna.</p>

      <p>El nombre es un homenaje a la revolución del 25 mayo de 1810, cuando los vecinos porteños se reunieron en la
        plaza para expulsar al virrey y nombrar el primer gobierno de criollos. Seis años más tarde, en la plaza se juró
        la independencia nacional y en 1860 la Constitución.</p>

      <p>Desde mediados del siglo XX es escenario de grandes manifestaciones sociales y, a partir de 1977, el lugar de
        reunión de las Madres de Plaza de Mayo, que reclaman la aparición de sus hijos secuestrados por la dictadura
        militar.</p>

      <p>En el centro de la plaza está la Pirámide de Mayo, un monumento que tiene forma de obelisco y fue construido en
        1811 para celebrar el primer aniversario de la revolución. En 1856 fue completamente reformada por el artista y
        arquitecto Prilidiano Pueyrredón. Tiene una altura de 19 metros.</p>

      <h2 class="destination-subtitle">Puente de la Mujer</h2>

      <p>Este puente peatonal es la primera obra del arquitecto e ingeniero valenciano Santiago Calatrava que se
        construye
        en América latina.</p>

      <p>Tiene 160 metros de largo y seis de ancho. Su mástil metálico se eleva 39 metros. El puente (de un peso
        aproximado de 800 toneladas) está preparado para moverse cada vez que una embarcación necesita pasar. Su
        mecanismo
        de giro es uno de los más grandes del mundo, con 20 motores eléctricos en el eje principal, que son comandados
        por
        computadora.</p>

      <p>La obra (donada a la ciudad por un empresario) se realizó en España y se trasladó por partes a Buenos Aires en
        varios embarques.</p>

      <p>El perfil plástico de la obra es interpretado por el autor como la figura de una pareja que baila tango, donde
        el
        mástil blanco representa al hombre y la silueta curva del puente a la mujer.</p>

      <h2 class="destination-subtitle">Reserva Ecológica y Costanera Sur</h2>

      <p>La Reserva Ecológica, con 350 hectáreas, es el mayor de los espacios verdes de Buenos Aires. La pueblan cientos
        de especies de la flora y fauna autóctonas de la llanura chaco-pampeana y de las orillas del Río de la
        Plata.</p>

      <p>Durante las décadas de 1970 y 1980 se realizaron rellenos con escombros junto a la costanera sur. Los terrenos
        ganados al río fueron colonizados por la flora y fauna locales y el área fue declarada Parque Natural y Zona de
        Reserva en 1986.</p>

      <p>En el paseo se pueden conocer los pastizales de cortaderas y bosques de alisos típicos de la región y distintas
        especies de aves, mamíferos, anfibios y reptiles.</p>

      <p>Los senderos de la Reserva, que llevan hasta el río, pueden recorrerse a pie o en bicicleta, o participando en
        una visita guiada.</p>

      <p>La Costanera Sur es uno de los paseos más populares de Buenos Aires desde hace casi un siglo. La gente va a la
        costanera a andar en bicicleta, a trotar, o a pasar el día en familia.</p>

      <p>En 1918 se inauguró en la costanera el balneario municipal y sus playas se convirtieron en las más concurridas
        del Río de la Plata. En las décadas de 1940 y 1950, en la zona abrieron restaurantes y salas de espectáculos
        donde
        a la noche actuaban los tangueros y actores cómicos de moda, como Aníbal Troilo o José "Pepitito" Marrone. A
        partir de 1960 el paseo perdió importancia, sobre todo por la contaminación del río, que llevó a la prohibición
        de
        bañarse en sus aguas. En las décadas de 1990 y 2000, la costanera y los parques de los bulevares fueron
        restaurados.</p>

      <p>La costanera es muy visitada de día y de noche, especialmente los fines de semana. En sus anchas veredas
        funcionan numerosos "carritos": puestos de comida al paso que se especializan en sándwiches de bondiola (carne
        de
        cerdo).</p>

      <h2 class="destination-subtitle">Teatro Colon</h2>

      <p>Inaugurado el 25 de mayo de 1908, el Teatro Colón en Buenos Aires, Argentina, es considerado uno de los cinco
        teatros más grandes del mundo, diseñado por Francesco Tamburini y Víctor Meano. El día de su inaguración se
        interpreto Aída de Verdi.</p>

      <p>Se encuentra ubicado en la Plaza Lavalle (San Nicolás), en el mismo lugar donde se encontraba la Estación del
        Parque del Ferrocarril Oeste, cabecera de la primera línea ferroviaria de la Argentina. Con capacidad para 3000
        personas y en un entorno de estilo ecléctico, mezcla de neorenacentismo italiano y de barroco francés, el diseño
        del teatro presenta una rica decoración en dorado y escarlata. La cúpula muestra figuras pintadas por el
        reconocido artista contemporáneo Raúl Soldi.</p>

      <p>A lo largo de su historia el Teatro Colón ha albergado grandes personajes como Arturo Toscanini, Jane Bathori,
        Enrico Caruso, Maria Callas, Plácido Domingo, Luciano Pavarotti, Ástor Piazzolla y Mercedes Sosa. También es
        habitual escenario de espectáculos de ballet y, ocasionalmente, de actos oficiales y privados y de música
        popular.
        Su estatus artístico está arraigado de tal forma en el imaginario colectivo argentino que se saluda al grito de
        ¡Al Colón! a los triunfadores de las más diversas expresiones culturales.</p>

      <p>Desde noviembre de 1989 el teatro es uno de los monumentos históricos nacionales.</p>

      <p>Dada la precaria situación económica argentina, en los últimos años el Teatro ha vivido zozobra, siendo atacado
        por algunos sectores que han cuestionado la financiación estatal de su elevado presupuesto. En la actualidad, en
        2006, se está encarando una remodelación integral de la sala, por lo cual su actividad será limitada y cerrará
        sus
        puertas hasta el 25 de mayo de 2008 cuando se reabrirá para el centenario de su inauguración.</p>

      <h2 class="destination-subtitle">Teatro San Martin</h2>

      <p>El Teatro General San Martín es un teatro de la Ciudad de Buenos Aires ubicado en la céntrica Avenida
        Corrientes.
        Es uno de los teatros más importantes de la Argentina y ofrece lugares para la representación de obras escénicas
        y
        cinematográficas, y diferentes exposiciones artísticas.</p>

      <p>Los proyectos para la construcción de este teatro datan de 1908 cuando el diputado Alfredo Palacios presenta un
        proyecto de ley que no trataron las cámaras. Sucede algo parecido con una ordenanza que sancionó el Consejo
        Deliberante de la Ciudad de Buenos Aires, que ordenaba la creación del Teatro Popular de Buenos Aires, que no
        promulgó el Intendente Joaquín de Anchorena.</p>

      <p>El 26 de noviembre de 1936 el intendente Mariano de Vedia y Mitre dispone la construcción de un edificio para
        alojar el Teatro del Pueblo, dirigido por Leónidas Barletta. El 23 de diciembre de ese mismo año se expropió un
        teatro ubicado en Av. Corrientes 1530, el Teatro Nuevo, y Barletta recibe una concesión de 25 años, que sería
        interrumpida el 3 de diciembre de 1943, luego del golpe de estado ocurrido ese mismo año.</p>

      <p>El 23 de mayo de 1944 se inauguró en el mismo lugar el Teatro Municipal de Buenos Aires para el fomento y
        difusión del teatro argentino. El teatro recibió en nuevo nombre de Teatro General San Martín, y el edificio
        actual comenzó a construirse el 24 de junio de 1954 con un proyecto de los arquitectos Mario Álvarez y Macedonio
        Ruiz. El nuevo edificio se inauguró finalmente el 25 de mayo de 1960, pero comenzó a operar un año después. El
        nuevo edificio lo convirtió en uno de los centros culturales más influyentes de Latinoamérica, ya que en los
        treinta mil metros cuadrados distribuidos en trece pisos y cuatro subsuelos, el edificio posee tres salas
        teatrales, varios salones de exposición y un cine.</p>

      <p>El teatro cuenta con tres salas para representaciones artísticas: una sala para teatro de comedia con capacidad
        para 1128 espectadores, una sala para teatro de cámara con capacidad para 576 espectadores y una sala para
        pequeñas representaciones con una capacidad para 200 personas.</p>

      <p>Pero en el teatro también hay una sala de cine con capacidad para 250 espectadores, una fotogalería de 300 m2,
        una escuela de arte dramático, y los diferentes talleres que producen lo necesario para la puesta en escena:
        ecenografía, utilería, peluquería, sastrería, zapatería, luminotecnia, fotografía, grabación y video, tapicería,
        escultura, pintura, efectos especiales electromecánicos y maquinaria escénica.</p>

      <h2 class="destination-subtitle">Zoologico y Jardin Botanico</h2>

      <p><strong>Zoológico</strong>: En 1888, la municipalidad de Buenos Aires dispuso que una porción del parque Tres
        de
        Febrero (los bosques de Palermo) se destinara a un jardín zoológico. Cuando abrió, contaba con 650 animales de
        53
        especies de todo el mundo.</p>

      <p>El zoológico (que cubre 18 hectáreas) fue privatizado en 1991 y se le hizo una gran remodelación. Actualmente
        cuenta con un acuario (con lobos marinos, pingüinos, tiburones y peces tropicales), un reptilario y un paseo que
        reproduce el ambiente de una selva subtropical.</p>

      <p><strong>Jardín Botánico</strong>: Se inauguró en 1898. Fue diseñado por el paisajista francés Carlos Thays,
        autor
        del trazado de algunos de los espacios verdes más importantes de Buenos Aires, como el Parque 3 de Febrero y la
        Plaza San Martín.</p>

      <p>Ocupa una superficie de más de 7 hectáreas y alberga unas 6.000 especies vegetales; cuenta con una biblioteca
