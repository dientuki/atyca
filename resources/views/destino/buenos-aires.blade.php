    <h1 class="destination-title">Buenos Aires: Información turistica</h1>

    <div class="destination-content">
      <p>La Ciudad de <strong>Buenos Aires</strong>, también denominada según su constitución desde 1996 como Ciudad
        Autónoma de Buenos Aires, es la Capital de la República Argentina. De manera no formal se utiliza también la
        denominación de Capital Federal.</p>

      <p>Se extiende sobre un terreno llano, y tiene una superficie de 202 km cuadrados. Viven allí aproximadamente 3
        millones de personas. Sumando la población del área metropolitana, la población total supera los 10 millones de
        habitantes, lo que la convierte en uno de los 10 centros urbanos más poblados del mundo.</p>

      <p>El Río de la Plata y el Riachuelo son sus límites naturales hacia el este y el sur. La avenida de
        circunvalación
        General Paz, que bordea la ciudad de norte a oeste, completa el límite de la ciudad. Esta avenida enlaza de
        manera
        rápida la capital con el Gran Buenos Aires, una franja de alta concentración de habitantes y fuerte
        actividad</p>

      <p>El clima de Buenos Aires es benigno todo el año. La temperatura media anual de 18º torna escasos los días de
        calor y frío intensos, lo que permite pasear por la ciudad en cualquier época.</p>

      <p>El mes más frío es julio. Aunque no se presentan heladas es necesario salir con un abrigo de lana, una campera
        o
        sobretodo y bufanda. En invierno el frío es moderado durante el día, pero por las noches baja considerablemente
        la
        temperatura.</p>

      <p>En verano, el calor es húmedo. Las mañanas son calurosas, muy calurosos los mediodías y las primeras horas de
        la
        tarde. A la noche, la temperatura desciende levemente, por lo que se usa ropa fresca y liviana, y no hay
        necesidad
        de abrigos.</p>

      <p>En los días soleados de otoño y primavera las mañanas son frescas, la temperatura sube en forma agradable hacia
        el mediodía y desciende por la noche.</p>

      <h2 class="destination-subtitle">Buenos Aires fue fundada dos veces:</h2>

      <p>La primera en 1536. Don Pedro de Mendoza, colonizador español, estableció el primer asentamiento. Lo nombró:
        Ciudad del Espíritu Santo y Puerto Santa María del Buen Ayre. La segunda –y definitiva–, en 1580. Juan de Garay
        denominó el sitio Ciudad de Trinidad.</p>

      <p>En el siglo XIX el puerto fue el punto de llegada de la gran corriente inmigratoria promovida por el Estado
        argentino para poblar la nación. Españoles, italianos, sirio-libaneses, polacos y rusos le imprimieron a Buenos
        Aires el eclecticismo cultural que la distingue.</p>

      <p>A lo largo del siglo XX, sucesivas migraciones —internas, de países latinoamericanos y de Oriente, terminaron
        de
        conformar a Buenos Aires como una ciudad cosmopolita en la que conviven personas de diversas culturas y
        religiones.</p>

